print("Welcome to Treasure Island.")
print("Your mission is to find the treasure.")

playerDirection = input("Which way do you want to go? (right or left): ").lower()

if playerDirection == "left":
    playerAction = input("Do you want to swim or wait? ").lower()

    if playerAction == "wait":
        doorChoice = input("Which door? (blue, yellow or red): ").lower()

        if doorChoice == "red":
            print("Burned by fire. Game over!!")
        elif doorChoice == "blue":
            print("Eaten by beasts. Game over!!")
        elif doorChoice == "yellow":
            print("You win!!")
        else:
            print("Game over!!")
    else:
        print("You were attacked by a trout. Game over!!")
else:
    print("You fell into a hole. Game over!!")